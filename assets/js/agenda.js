// Function to close the popup
function closePopup() {
    var popup = document.getElementById("popup");
    popup.style.display = "none";
}

// Close the popup if user clicks outside of it
window.onclick = function (event) {
    var popup = document.getElementById("popup");
    if (event.target == popup) {                           
        popup.style.display = "none";
    }
};
